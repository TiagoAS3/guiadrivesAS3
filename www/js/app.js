    function buscafalha() {

        $("#desc-area").slideUp(400);
        $("#sol-area").slideUp(400);
        $("#causa-area").slideUp(400);

        var fb_descricao = '';
        var fb_causa = '';
        var fb_solucao = '';
        var fb_marca = '';
        var fb_modelo = '';


        var codFalha = 0;
        codFalha = document.getElementById("buscaFalha").value;
        codFalha = codFalha.toUpperCase(); // converte para Letras MAIUSCULAS


        var read = firebase.database().ref('falhas/' + codFalha);
        read.on("value", snap => {

            fb_descricao = snap.child("significado").val(); //recebe string significado
            fb_causa = snap.child("causa").val(); //recebe string causa
            fb_solucao = snap.child("solucao").val(); //recebe string solucao
            fb_marca = snap.child("marca").val(); //recebe string smarca
            fb_modelo = snap.child("modelo").val(); //recebe string modelo





            setTimeout(function() {

                if (fb_descricao == 'null' || fb_descricao == null) {
                    p_descricao.innerHTML = "";
                    p_modelo.innerHTML = "";
                    console.log("Falha/Alarme não encontrado");
                    falhaNaoEncontrada.innerHTML = "<strong> Sua pesquisa: " + codFalha + "</strong>, não encontrou nenhum resultado correspondente. <br> <br> <strong> Sugestões:</strong> <br> - Certifique-se que o codigo da Falha ou Alarme estejam escritos corretamente. <br> - Entre em contato com a AS3.";
                    $("#desc-area").slideDown();

                } else {


                    falhaNaoEncontrada.innerHTML = "";

                    p_descricao.innerHTML = "<b>" + codFalha + "</b>: " + fb_descricao;
                    $("#desc-area").slideDown();
                    p_modelo.innerHTML = "<b>Drives correspondentes à Falha/Alarme:</b> <br>" + fb_marca + " - " + fb_modelo;

                    if (fb_causa + '!' == '!') {
                        p_causa.innerHTML = "";
                        console.log("causa indisponivel");

                    } else {
                        p_causa.innerHTML = "<b>Causas Prováveis:</b> " + fb_causa;
                        $("#causa-area").slideDown();
                    }

                    if (fb_solucao + '!' == '!') {
                        console.log("solucao indisponivel");
                        p_solucao.innerHTML = "";
                    } else {

                        p_solucao.innerHTML = "<b>Solução:</b> " + fb_solucao;
                        $("#sol-area").slideDown();

                    }

                }

            }, 500);


        });

    }

